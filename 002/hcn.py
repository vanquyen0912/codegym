# B1: Nhúng thư viện
import turtle
# B2 Tạo đối tượng
t= turtle.Turtle()
# B3: Vẽ ngang
t.forward(100)
# B4: quay phải 90 độ
t.right(90)
# b5: vẽ ngang
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.penup()
t.goto(-150,0)
t.pendown()
t.forward(200)
t.right(90)
t.forward(100)
t.right(90)
t.forward(200)
t.right(90)
t.forward(100)
turtle.done()